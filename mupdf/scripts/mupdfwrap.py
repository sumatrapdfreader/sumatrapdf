def snake_to_camel( name, initial):
    '''
    Converts foo_bar to FooBar or fooBar.
    '''
    items = name.split( '_')
    ret = ''
    for i, item in enumerate( items):
        if not item:
            item = '_'
        elif i or initial:
            item = item[0].upper() + item[1:]
        ret += item
    return ret

assert snake_to_camel( 'foo_bar', True) == 'FooBar'
assert snake_to_camel( 'foo_bar_q__a', False) == 'fooBarQ_A'


def clip( text, prefix, suffix=''):
    '''
    Returns <text> with prefix and suffix removed if present.
    '''
    if text.startswith( prefix):
        text = text[ len( prefix):]
    if suffix and text.endswith( suffix):
        text = text[ :-len( suffix)]
    return text

